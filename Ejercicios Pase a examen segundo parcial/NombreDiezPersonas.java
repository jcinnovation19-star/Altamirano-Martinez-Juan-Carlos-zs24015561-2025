public class NombreDiezPersonas{
    public static void main(String[] args){
        String[] nombres = {"Julian", "Pedro", "jose", "Antonio", "Lorena",
            "Alex", "Heidi", "Judith", "Elena", "Dora" };
        int i = 0; 
        while (i < 10) {
            System.out.println((i + 1) + ". " + nombres[i]);
            i++;
                
        }
        
    }
}